package ex_02_operators;

public class L001_Arithmetic_operator {
    public static void main(String[] args) {
        int a=200;
        int b=10;

        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);
        System.out.println(a%b);
        System.out.println(++b);
        System.out.println(--b);



    }
}
