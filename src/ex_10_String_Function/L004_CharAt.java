package ex_10_String_Function;

public class L004_CharAt {
    public static void main(String[] args) {
        String name="Ajaypali ";
        System.out.println(name.trim());
        System.out.println(name.length());
        System.out.println(name.concat("Nepal"));
        System.out.println(name.contains("Ajaypali"));
        System.out.println(name.charAt(4));
        System.out.println(name.contains("Nepal"));
    }
}
